def area(a, h): 
    '''returns area of triangle with given side and height
        input:
            a (int): side of square
            h (int): height of triangle
        
        return:
            s (int): area of triangle
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''returns perimeter of triangle with three given sides
        input:
            a (int): 1st side of triangle
            b (int): 2nd side of triangle
            c (int): 3rd side of triangle
        
        return:
            p (int): perimeter of triangle
    '''
    return a + b + c 
