def area(a):
    '''returns area of square with given side
        input:
            a (int): side of square
        
        return:
            s (int): area of square
    '''
    return a * a


def perimeter(a):
    '''returns perimeter of square with given side
        input:
            a (int): side of square
        
        return:
            p (int): perimeter of square
    '''
    return 4 * a
