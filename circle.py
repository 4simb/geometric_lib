import math


def area(r):
    '''returns area of circle with given radius
        input:
            r (int): radius
        
        return:
            s (int): area of circle
    '''
    return math.pi * r * r


def perimeter(r):
    '''returns perimeter of circle with given radius
        input:
            r (int): radius
        
        return:
            p (int): perimeter of circle
    '''
    return 2 * math.pi * r

